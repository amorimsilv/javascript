let loop;
for (loop = 0; loop < 10; loop++) {
    console.log(loop)
}

// o Script DEVE ser lido da seguinte forma: a let loop, na primeira linha, se encontra VAZIA
// logo apos, na linha 2, vai declarar que o loop RECEBE (= recebe) 0, então o loop vai começar a valer 0
// logo depois, se o loop for MENOR (<) que 10, ele ira adicionar +1 (loop++)
// Então, enquanto o loop não chegar no 10, ele irá adicionar +1
// Só que, o maximo que vai, com o loop < 10, é no nove, caso queira que chegue literalmente no 10, coloca o 11 no lugar do 10
